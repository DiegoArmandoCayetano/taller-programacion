    private void btnSumarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSumarActionPerformed
        if(txtMostrar.getText().isEmpty()){
            JOptionPane.showMessageDialog(rootPane, "Ingrese primero un numero");
        }else{
            calculadora1.setNumero1(Double.parseDouble(this.txtMostrar.getText()));
        operador="+";
        this.txtMostrar.setText("");
        }
        
    }//GEN-LAST:event_btnSumarActionPerformed

    private void btnOperarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnOperarActionPerformed

        calculadora1.setNumero2(Double.parseDouble(this.txtMostrar.getText()));
        
        switch(operador){
            case "+":this.txtMostrar.setText(String.valueOf(calculadora1.getSuma()));
            break;
            case "-":this.txtMostrar.setText(String.valueOf(calculadora1.getResta()));
            break;
            case "*":this.txtMostrar.setText(String.valueOf(calculadora1.getMultiplicacion()));
            break;
            case "/":this.txtMostrar.setText(String.valueOf(calculadora1.getDivision()));
            break;

        }
    }//GEN-LAST:event_btnOperarActionPerformed

