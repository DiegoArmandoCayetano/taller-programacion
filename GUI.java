    private void btnDivisionActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDivisionActionPerformed
        
        if (txtMostrar.getText().isEmpty()) {
    JOptionPane.showMessageDialog(rootPane, "Ingrese primero un número");
} else {
    double num_1 = Double.parseDouble(txtMostrar.getText());
     double num_2 = Double.parseDouble(txtMostrar.getText());
    if (num_1 == 0 || num_2 ==0) {
        JOptionPane.showMessageDialog(rootPane, "No se puede dividir entre cero");
    } else {
        calculadora1.setNumero1(num_1);
        operador = "/";
        txtMostrar.setText("");
    }
}

    }//GEN-LAST:event_btnDivisionActionPerformed

